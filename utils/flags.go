// NewApp creates an app with sane defaults.
var app  *cli.App = nil

func NewApp(gitCommit, usage string) *cli.App {
	if app != nil {
		return app
	}
	app := cli.NewApp()
	app.Name = filepath.Base(os.Args[0])
	app.Author = ""
	//app.Authors = nil
	app.Email = ""
	app.Version = "1.2"
	if len(gitCommit) >= 8 {
		app.Version += "-" + gitCommit[:8]
	}
	app.Usage = usage
	return app
}

